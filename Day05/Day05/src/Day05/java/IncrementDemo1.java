package Day05.java;

public class IncrementDemo1 {

	public static void main(String[] args) {
		int i=1;
		System.out.println("Post Incremnet");
		System.out.println(i++);
		System.out.println(i);
		int j=1;
		System.out.println("Pre Incremnet");
		System.out.println(++j);
		System.out.println(j);

	}

}
