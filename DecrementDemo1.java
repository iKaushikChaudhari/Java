package Day05.java;

public class DecrementDemo1 {

	public static void main(String[] args) {
		int i=1;
		System.out.println("Post Decremnet");
		System.out.println(i--);
		System.out.println(i);
		int j=1;
		System.out.println("Pre Decremnet");
		System.out.println(--j);
		System.out.println(j);

	}

}
